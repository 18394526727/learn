/**
 * 使用[a-z]类匹配a-z之间的所有小写英文字符
 * 使用[A-Z]类匹配a-z之间的所有大写英文字符
 * 使用[0-9]类匹配a-z之间的所有数字
 * 
 */
var str = 'a1b2c3d4e5';
console.log(str.replace(/[a-z]/g,"嗯嗯"))//嗯嗯1嗯嗯2嗯嗯3嗯嗯4嗯嗯5
var str = 'a1-b2-c3-d4-e5-A-B-C';
console.log(str.replace(/[a-zA-Z0-9]/g,"嗯。"));
// 嗯。嗯。-嗯。嗯。-嗯。嗯。-嗯。嗯。-嗯。嗯。-嗯。-嗯。-嗯。
var str = 'a1-b2-c3-d4-e5-A-B-C';
/**
 * 想要匹配到-，怎么办呢?，在[]里面加一个-即可
 */
console.log(str.replace(/[a-zA-Z0-9-]/g,"哦."));
// 哦.哦.哦.哦.哦.哦.哦.哦.哦.哦.哦.哦.哦.哦.哦.哦.哦.哦.哦.哦.